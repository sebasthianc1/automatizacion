public class Calc {
	// metodos
	public int  sumar(int cant1, int cant2) {
	return  cant1 + cant2;
	}
    
	public int  resta(int cant1, int cant2) {
	return  cant1 - cant2;
	}
    public int  multiplicacion(int cant1, int cant2) {
	return  cant1 * cant2;
    }
    public int  division(int cant1, int cant2) {
	return  (int) (cant1 / cant2);
    }

}
